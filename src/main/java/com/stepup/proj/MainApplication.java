    public static String age(int x) {
        // Получаем остаток от деления на 100, чтобы проверить особые случаи 11-14
        int lastTwoDigits = x % 100;
        // Получаем последнюю цифру числа
        int lastDigit = x % 10;
        // Проверяем особые случаи для чисел 11-14
        if (lastTwoDigits >= 11 && lastTwoDigits <= 14) return x + " лет";
        // Проверяем остальные случаи
        if (lastDigit == 1) return x + " год";
        if (lastDigit >= 2 && lastDigit <= 4) return x + " года";
        //все остальные случаи
        return x + " лет";
    }

    public static boolean magic6(int x, int y) {
        // Проверяем, равно ли одно из чисел 6
        if (x == 6 || y == 6) return true;

        // Проверяем, равна ли сумма чисел 6
        if (x + y == 6) return true;

        // Проверяем, равна ли разница между числами 6
        // Используем Math.abs() для корректной работы с отрицательными числами
        if (Math.abs(x - y) == 6) return true;

        // Если ни одно из условий не выполнено
        return false;
    }
    public static boolean is35(int x) {
        // Проверяем делимость на 3
        boolean divisibleBy3 = (x % 3 == 0);

        // Проверяем делимость на 5
        boolean divisibleBy5 = (x % 5 == 0);

        // Возвращаем true только в двух случаях:
        // 1. Если число делится только на 3 (но не на 5)
        // если в обоих случаях будет true - то ! перевернёт в false, и итоговый будет false
        boolean divisibleBy3not5 = (divisibleBy3 && !divisibleBy5);

        // 2. Если число делится только на 5 (но не на 3)
        // тоже самое, если в обоих случаях будет true - то итогом будет false
        boolean divisibleBy5not3 = (!divisibleBy3 && divisibleBy5);

        //если делится на 3 - true или false - true
        //если делится на 5 - false или true - true
        //если делится на обо числа то результат будет false или false - false
        return divisibleBy3not5 || divisibleBy5not3;
    }

