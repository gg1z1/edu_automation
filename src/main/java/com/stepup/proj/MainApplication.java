    public static boolean sum3(int x, int y, int z) {
        // Всего возможны три комбинации
        // Собственно если хоть одна из комбинаций срабатывает то true
        // конструкция - условие ИЛИ условие ИЛИ условие
        return (x + y == z) || (x + z == y) || (y + z == x);
    }

